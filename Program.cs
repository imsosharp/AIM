namespace AiM
{
    class Program
    {
        static void Main(string[] args)
        {
            var plugin = Type.GetType("AiM.Plugins." + ObjectManager.Player.ChampionName);
            try
            {
                if (plugin == null)
                {
                    plugin = Type.GetType("AiM.Plugins.Default");
                }
                if (plugin == null)
                {
                    return;
                }
                Activator.CreateInstance(plugin);
            }
            catch (Exception e)
            {
                Console.WriteLine(e);
            }
                
        }
    }
